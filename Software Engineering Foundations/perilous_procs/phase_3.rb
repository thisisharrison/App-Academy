def picky_procipher(str, prcs)
    words = str.split(" ")
    new_str = words.map do |word|
        new_word = word
        prcs.each do |key_prc, val_prc|    
            if key_prc.call(word) 
                new_word = val_prc.call(new_word) 
                break 
            end
        end
        new_word
    end
    new_str.join(" ")
end
