def proctition_platinum(arr, *prcs)
    result = Hash.new {|h,k| h[k] = []}
    arr.each do |ele|
        prcs.each_with_index do |prc, i|
            if prc.call(ele)
                result[i+1] << ele
                break
            end
        end
    end
    result.sort.to_h
end

